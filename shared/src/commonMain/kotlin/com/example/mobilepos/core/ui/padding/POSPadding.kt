package com.example.mobilepos.core.ui.padding

/// Defines legal padding values for the POS application.
///
/// These values are used to maintain consistent spacing throughout the UI components,
/// and should be used instead of hardcoding padding values directly in the UI components.
class POSPadding {
    companion object {
        const val EXTRA_SMALL: Float = 2.0f
        const val SMALL: Float = 4.0f
        const val DEFAULT: Float = 8.0f
        const val MEDIUM: Float = 12.0f
        const val LARGE: Float = 16.0f
    }
}